'use client';

import { useAuth } from '@/lib/auth-context';
import { useNativeGoogleLogin } from '@/lib/useNativeGoogleLogin';
import { GoogleLogin } from '@react-oauth/google';
import Link from 'next/link';
import { useRouter, useSearchParams } from 'next/navigation';
import { useState, useEffect, Suspense } from 'react';

function GoogleIcon() {
  return (
    <svg width="20" height="20" viewBox="0 0 48 48" aria-hidden="true">
      <path fill="#EA4335" d="M24 9.5c3.54 0 6.71 1.22 9.21 3.6l6.85-6.85C35.9 2.38 30.47 0 24 0 14.62 0 6.51 5.38 2.56 13.22l7.98 6.19C12.43 13.72 17.74 9.5 24 9.5z"/>
      <path fill="#4285F4" d="M46.98 24.55c0-1.57-.15-3.09-.38-4.55H24v9.02h12.94c-.58 2.96-2.26 5.48-4.78 7.18l7.73 6c4.51-4.18 7.09-10.36 7.09-17.65z"/>
      <path fill="#FBBC05" d="M10.53 28.59a14.5 14.5 0 0 1 0-9.18l-7.98-6.19a24.01 24.01 0 0 0 0 21.56l7.98-6.19z"/>
      <path fill="#34A853" d="M24 48c6.48 0 11.93-2.13 15.89-5.81l-7.73-6c-2.15 1.45-4.92 2.3-8.16 2.3-6.26 0-11.57-4.22-13.47-9.91l-7.98 6.19C6.51 42.62 14.62 48 24 48z"/>
    </svg>
  );
}

function LoginForm() {
  const { user, login, loading } = useAuth();
  const { isNative, nativeLogin, initializing, error: nativeError } = useNativeGoogleLogin();
  const router = useRouter();
  const searchParams = useSearchParams();
  const [error, setError] = useState('');
  const [signingIn, setSigningIn] = useState(false);

  const returnUrl = searchParams.get('returnUrl') || '/';

  useEffect(() => {
    if (user) {
      router.push(returnUrl);
    }
  }, [user, router, returnUrl]);

  if (loading || initializing) return null;
  if (user) return null;

  const handleNativeLogin = async () => {
    setError('');
    setSigningIn(true);
    try {
      const idToken = await nativeLogin();
      await login(idToken);
    } catch (err) {
      setError(err instanceof Error ? err.message : 'Login failed');
    } finally {
      setSigningIn(false);
    }
  };

  return (
    <div className="login-screen">
      <Link href={returnUrl} className="login-back-link" aria-label="Go back">
        &larr; Back
      </Link>
      <div className="login-brand">
        {/* eslint-disable-next-line @next/next/no-img-element */}
        <img src="/icons/logo.svg" alt="Learn to Play" className="login-logo" />
        <h1 className="login-title">Learn to Play</h1>
        <p className="login-tagline">Interactive teaching guides for board games</p>
      </div>

      <div className="login-action">
        {(error || nativeError) && <p className="login-error">{error || nativeError}</p>}

        <div className="login-google-btn">
          {isNative ? (
            <button
              type="button"
              className="login-native-google-btn"
              onClick={handleNativeLogin}
              disabled={signingIn}
            >
              <GoogleIcon />
              <span>{signingIn ? 'Signing in\u2026' : 'Sign in with Google'}</span>
            </button>
          ) : (
            <GoogleLogin
              onSuccess={async (response) => {
                try {
                  if (response.credential) {
                    await login(response.credential);
                  }
                } catch (err) {
                  setError(err instanceof Error ? err.message : 'Login failed');
                }
              }}
              onError={() => setError('Google sign-in failed')}
              theme="filled_black"
              shape="rectangular"
              size="large"
            />
          )}
        </div>
        <p className="login-fine-print">Sign in with Google to access your guides</p>
      </div>
    </div>
  );
}

export default function LoginPage() {
  return (
    <Suspense>
      <LoginForm />
    </Suspense>
  );
}
